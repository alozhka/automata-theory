namespace Runtime;

public enum ValueType
{
    /// <summary>
    /// Значение отсутствует.
    /// </summary>
    Void,

    /// <summary>
    /// Целочисленное значение.
    /// </summary>
    Int,

    /// <summary>
    /// Строковое значение.
    /// </summary>
    String,

    /// <summary>
    /// Значение числа с плавающей точкой.
    /// </summary>
    Double,
}